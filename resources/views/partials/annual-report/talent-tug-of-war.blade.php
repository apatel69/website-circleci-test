        <div class="arContainer">
            <h3>Barriers to self-employment also vary by generation</h3>
            <p>Millennials fear income inconsistency, Gen Xers worry about making less money, and Boomers don’t want to lose their health benefits. Notably, what is not keeping people in their current full-time positions is employer loyalty. Just one-in-five traditional employees say loyalty to their current company is stopping them from becoming self-employed.</p> 

            <div class="talent__twoCol">
                <h3>Which employees are most likely to become self-employed?</h3>
                <p>If you work at a small business, chances are you’re considering self-employment. In fact, 35% of people working at companies with fewer than 50 employees say it’s somewhat to very likely they’ll become self-employed in the next two years, compared to 25% of people working at larger companies. This corresponds directly with another of our key findings: that small business owners struggle with finding talented staff or contractors more so than any other challenge, including finding new customers.</p>
                <p>Specifically, Millennials who work in senior roles, like C-suite execs and company presidents, are most likely to be contemplating a switch to self-employment. Functionally, we find that IT professionals are twice as likely as the average employee to be pondering self-employment in the near term. As many already command higher salaries from traditional employers, large numbers of IT professionals are eyeing self-employment as a means to greater career fulfillment vs. any other reason.</p>
