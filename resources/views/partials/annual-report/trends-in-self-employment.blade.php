                <p class="trends__mrdDescription">Lower median revenue is exactly what we would expect given a decline in average small business age and experience. Less clear is the relationship between self-employment income and career satisfaction. Past studies have shown older and more financially successful small business owners to be the most satisfied workers overall (i.e., whether traditionally or self-employed). This year’s rebound in satisfaction despite lower earnings may be indicative of shifting motivations away from the financial and towards career control, fulfillment and other non-monetary considerations.</p>
