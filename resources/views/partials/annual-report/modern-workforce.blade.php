        <h3>Conclusion</h3>
        <div class="modernWorkforce__conclusionCurve"></div>
        <p>Last year we concluded that a significant shift in the American workforce was underway, as tens of millions of workers seemed ready to take the self-employment plunge. And while many did just that over the past 12 months, many more did not. No doubt record low unemployment and continued economic growth in 2018 have played a role, as desperate employers do all they can to retain their best and brightest. But as we’ve learned this year, the primary motivators behind the shift are not financial nor are they easily accommodated within the confines of a ‘traditional’ job. Most are looking for some combination of career control, change and fulfillment that can only be realized by going it alone.</p>
        <p>We’ve also learned that prospective independents differ in their self-employment ‘readiness’ and the hard and soft barriers that keep them from pursuing their career aspirations. If there is one certainty though, it’s that the tools and resources needed to overcome these barriers will only become better and more accessible in the future. So we’re sticking with our prediction that self-employment is redefining the American dream. It just might take a little longer.</p>
